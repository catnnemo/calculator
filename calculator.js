function area_code(area) {
  if (area <= 0){
      return 0;
    } else if (area<=2000) {
      return "u2";
    } else if (area<=4000) {
      return "u4";
    } else if (area<=6000){
      return "u6";
    } else if (area<=8000){
      return "u8";
    } else if (area <= 15000){
      return "u15";
    } else if (area > 15000){
      return "s";
    } else if (area.value.length == 0){
      return 0;
    } else {
      return 0;
    }
}

